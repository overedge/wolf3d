#include "ft_printf.h"

void	flags_effect(char *fmt, t_var *e)
{
	if (fmt[e->i] == '#' && fmt[e->i + 1] == '\0')
		e->error = 1;
	else if (fmt[e->i] == '#')
	{
		while (fmt[e->i] == '#')
		{
			e->f_effect = 1;
			e->i++;
		}
	}
}
